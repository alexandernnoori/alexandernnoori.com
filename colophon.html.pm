This website’s design is based on the websites of Matthew Butterick — in particular ◊link["https://practicaltypography.com" ◊book{Practical Typography}] and ◊link["https://typographyforlawyers.com" ◊book{Typography for Lawyers}]—whose text-first, readable approach to web design has shaped how I think about presenting words on a screen. It is built with ◊link["https://github.com/mattbutterick/pollen" "Pollen,"] an open-source publishing system also by Butterick. Butterick’s writing on typography and typesetting has been a quiet influence on my work as a lawyer for years, and I’m grateful for it.

The fonts used on this site are all designed by Butterick and available through his foundry, ◊link["https://mbtype.com" "MB Type."]

